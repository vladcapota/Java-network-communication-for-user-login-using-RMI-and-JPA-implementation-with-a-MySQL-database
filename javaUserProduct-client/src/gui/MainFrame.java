    private DefaultListModel<ProductDto> model = new DefaultListModel<>();
    
    public MainFrame() {
        
        initComponents();
        jList1.setModel(model);
        getProducts();
        
        jList1.addMouseListener(new MouseAdapter(){
            
            @Override
            public void mouseClicked(MouseEvent ev){
                
                if(ev.getClickCount() == 2){
                    try{
                        
                        ProductDto productDto = jList1.getSelectedValue();
                        ProductController.getInstance().deleteProduct(productDto);
                        getProducts();
                        
                    }catch(RemoteException ex){
                        JOptionPane.showMessageDialog(null, "Error - double click failed to process command. "
                                + "Please contaact your administrator");
                    }
                }
                
            }
            
            
        });
        
        jButton1.addActionListener(ev -> addProduct());
        
        setVisible(true);
        setLocationRelativeTo(null);
        
        
    }
    
    private void addProduct(){
        
        try{
            String name = jTextField1.getText();
            double price = Double.parseDouble(jTextField2.getText());
            ProductController.getInstance().addProduct(name, price);
            jTextField1.setText(null);
            jTextField2.setText(null);
            getProducts();
        }catch(Exception ex){
            ex.printStackTrace();
        }
        
    }
    
    private void getProducts(){
        
        try{
            model.clear();
            List<ProductDto> productList = ProductController.getInstance().getProducts();
            productList.forEach(model::addElement);
            
        }catch(Exception ex){
            ex.printStackTrace();
            
        }
        
    }


